
#s'introdueix la base
base = float(input("Introdueixi la base del triangle: ")) 

#s'introdueix l'altura
altura = float(input("Introdueixi la altura del triangle: "))

#es mosta en pantalla l'area del triangle utilitzant la formula base per altura divit 2
print("L'area del triangle és {}m²".format((base * altura) / 2))